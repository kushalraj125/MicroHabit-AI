# --- 2. Update toggle_habit to log the date ---
@app.route('/api/habits/<int:id>', methods=['PATCH'])
def toggle_habit(id):
    user_id = session.get('user_id')
    habit = Habit.query.filter_by(id=id, user_id=user_id).first()
    
    habit.completed = not habit.completed
    
    if habit.completed:
        # Record the completion for today
        new_log = CompletionLog(habit_id=id, date=datetime.utcnow().date())
        db.session.add(new_log)
    else:
        # If unchecked, remove today's log
        CompletionLog.query.filter_by(habit_id=id, date=datetime.utcnow().date()).delete()
        
    db.session.commit()
    return jsonify({"id": habit.id, "completed": habit.completed})

# --- 3. New Route for History ---
@app.route('/api/history', methods=['GET'])
def get_history():
    user_id = session.get('user_id')
    seven_days_ago = datetime.utcnow().date() - timedelta(days=6)
    
    # Get all logs for this user's habits in the last 7 days
    logs = db.session.query(CompletionLog.date, db.func.count(CompletionLog.id))\
        .join(Habit)\
        .filter(Habit.user_id == user_id, CompletionLog.date >= seven_days_ago)\
        .group_by(CompletionLog.date).all()
    
    # Format for the frontend: { "2026-02-28": 5, "2026-02-27": 3 ... }
    history_data = {str(log[0]): log[1] for log in logs}
    return jsonify(history_data)

